export const stringToCountryName = (input: string): CountryName => {
  const upperCased = input.toUpperCase()

  if (isKeyOfCountryName(upperCased)) return CountryName[upperCased]

  throw new Error(`Invalid country name: ${input}`)
}

export const isKeyOfCountryName = (key: string): key is keyof typeof CountryName =>
  Object.keys(CountryName).includes(key)
